package com.createchance.imageeditor;

/**
 * Histogram data class.
 *
 * @author createchance
 * @date 2018/12/22
 */
public class HistogramData {
    public int mRed, mGreen, mBlue, mAll;

    @Override
    public String toString() {
        return "HistogramData{" +
                "mRed=" + mRed +
                ", mGreen=" + mGreen +
                ", mBlue=" + mBlue +
                ", mAll=" + mAll +
                '}';
    }
}
